// while loop: 

let i = 0
while (i <= 10) {
    console.log(`Value of index is ${i}`);
    i = i + 2
}

let myArray = ['flash', "batman", "superman"]
let len = 0
while (len < myArray.length) {
    //console.log(`Value is ${myArray[arr]}`);
    len = len + 1
}

// do-while loop:

let score = 11
do {
    console.log(`Score is ${score}`);
    score++
} while (score <= 10);